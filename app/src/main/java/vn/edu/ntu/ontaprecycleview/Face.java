package vn.edu.ntu.ontaprecycleview;

public class Face {
    private String mName;
    private int mResID;

    public Face(String name, int ResId) {
        this.mName = name;
        this.mResID = ResId;
    }

    public String getmName() {
        return mName;
    }

    public int getmResID() {
        return mResID;
    }
}
